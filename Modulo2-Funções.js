function soma(a,b){
    return a+b;
}
function sub(a,b){
    return a-b;
}
function mult(a,b){
    return a*b;
}
function div(a,b){
    return a/b;
}
function MostraResultado(){
    a= 15
    b= 5
    console.log("Soma: " + soma(a,b));
    console.log("Subtração:" + sub(a,b));
    console.log("Multiplicação:" + mult(a,b));
    console.log("Divisão:" + div(a,b));
}
MostraResultado();